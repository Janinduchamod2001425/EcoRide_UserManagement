import mongoose from "mongoose";

const vehicleSchema = mongoose.Schema(
  {
    license: {
      type: String,
      required: true,
    },
    model: {
      type: String,
      required: true,
    },
    year: {
      type: Number,
      required: true,
    },
    owner: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Vehicle = mongoose.model("ownvehicle", vehicleSchema);

export default Vehicle;
